//Utilizando la sentencia switch

var sign = prompt("what is your astrological sign?");

switch(sign){
	case "Taurus":
		alert("You are Taurus");
		break;
	case "Virgo":
		alert("You are Virgo");
		break;
	case "Leo":
		alert("You are Leo");
		break;
	case "Aries":
		alert("You are Aries");
		break;
	case "Scorpion":
		alert("You are scorpion");
		break;
	case "Libra":
		alert("You are Libra");
		break;
	default:
		alert("I dont know that astrological sign");
		break;
}